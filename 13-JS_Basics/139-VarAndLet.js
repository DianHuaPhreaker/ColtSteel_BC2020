// Variables - variables are like labels for values
// Can store a value and give it a name so that we can:
// refer back to it later
// user that value to do stuff
// or change it later

let year = 1985;
console.log(year); // 1985
year = 1990;
console.log(year); // 1990

let numOfHens = 4;
let numOfRoosters = 5;
console.log(numOfHens + numOfRoosters); // 9
let totalChicken = numOfHens + numOfRoosters;
console.log(totalChicken);

